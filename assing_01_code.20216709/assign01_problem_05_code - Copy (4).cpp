#include <iostream>
using namespace std;
 
int main() {
    int x, y;
    cin >> x >> y;
    int summation = x + y;
    int multiplication  = x * y;
    int subtraction  = x - y;
    cout <<"X + Y ="<< summation << endl;
    cout <<"X * Y ="<< multiplication  << endl;
    cout <<"X - Y ="<< subtraction  << endl;
    return 0;
}
